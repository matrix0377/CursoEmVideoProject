n = input('Digite um valor: ')
print(type(n))
print('O digito é alfabetico?',(n.isalpha()))
print('O digito é caixa baixa?',(n.islower()))
print('O digito é alfanumérico?',(n.isalnum()))
print('O digito é decimal?', (n.isdecimal()))
print('O digito é caixa Alta?', (n.isupper()))
print('O digito é Numérico?', (n.isnumeric()))

